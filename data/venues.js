// Venue type options for validation
export const venueTypes = [
  'music_venue',
  'brewery', 
  'coffee_shop',
  'restaurant',
  'bar',
  'event_space',
  'amphitheater',
  'theater',
  'arena'
]

// Helper function to validate venue data
export function validateVenue(venue) {
  const required = ['name', 'address', 'city', 'venue_type']
  const missing = required.filter(field => !venue[field])
  
  if (missing.length > 0) {
    throw new Error(`Missing required fields: ${missing.join(', ')}`)
  }
  
  if (!venueTypes.includes(venue.venue_type)) {
    throw new Error(`Invalid venue_type: ${venue.venue_type}. Must be one of: ${venueTypes.join(', ')}`)
  }
  
  return true
}

// Helper function to get venues by type
export function getVenuesByType(type) {
  return venues.filter(venue => venue.venue_type === type)
}

// Helper function to get venues by city
export function getVenuesByCity(city) {
  return venues.filter(venue => venue.city.toLowerCase() === city.toLowerCase())
}

// Helper function to get venues by capacity range
export function getVenuesByCapacity(minCapacity, maxCapacity) {
  return venues.filter(venue => 
    venue.capacity >= minCapacity && venue.capacity <= maxCapacity
  )
}

// Helper function to search venues by genre
export function getVenuesByGenre(genre) {
  return venues.filter(venue => 
    venue.genres.some(g => g.toLowerCase().includes(genre.toLowerCase()))
  )
}

// Get venue statistics
export function getVenueStats() {
  const stats = {
    total: venues.length,
    byType: {},
    byCity: {},
    capacityRanges: {
      intimate: venues.filter(v => v.capacity <= 100).length,
      small: venues.filter(v => v.capacity > 100 && v.capacity <= 300).length,
      medium: venues.filter(v => v.capacity > 300 && v.capacity <= 1000).length,
      large: venues.filter(v => v.capacity > 1000).length
    }
  }
  
  // Count by type
  venueTypes.forEach(type => {
    stats.byType[type] = venues.filter(v => v.venue_type === type).length
  })
  
  // Count by city
  const cities = [...new Set(venues.map(v => v.city))]
  cities.forEach(city => {
    stats.byCity[city] = venues.filter(v => v.city === city).length
  })
  
  return stats
}